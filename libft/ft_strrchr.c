#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*rev;

	rev = 0;
	while (*s)
	{
		if (*s == c)
			rev = (char*)s;
		s++;
	}
	if (c == 0)
		return ((char*)s);
	return (rev);
}
