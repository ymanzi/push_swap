#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*b;

	if (!s)
		return (0);
	b = (char*)s;
	i = 0;
	while (i < n)
		b[i++] = c;
	return (s);
}
